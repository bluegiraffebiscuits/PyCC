def cheese_sentence(first_cheese, second_cheese): #That's the parameter
    """Display what your 2 favorite cheeses are."""
    print("\nA cheese I like is " + first_cheese.title() + " cheese.")
    print("Another cheese I like is " + second_cheese.title() + " cheese.")

cheese_sentence("american", "parmesan")   #<---That's the argument
cheese_sentence("swiss", "cheddar")